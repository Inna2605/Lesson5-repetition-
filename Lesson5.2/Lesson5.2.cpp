/*Створити програму, яка виводить на екран прості числа в діапазоні від 2 до 1000. 
(Число називається простим, якщо воно ділиться тільки на 1 і на саме себе без залишку; 
причому числа 1 і 2 за прості не вважаються).*/

#include<iostream>
using namespace std;
int main() {
	cout << "Prosti cgusla vid 2 do 1000: ";
	for (int j = 3; j < 1000; j++) {
		int a = 0;
		for (int i = 2; i < j; i++) {
			if (j % i == 0) {
				a++;
				break;
			}
		}
		if (a == 0)cout << j << ' ';
	}
}